struct MessageArgs {
    #[arg(
        short = 'm',
        long = "message",
        value_name = "MESSAGE",
        conflicts_with = "message_file",
        help = "Commit message to use (instead of the default autogenerated message)"
    )]
    message: Option<String>,

    #[arg(
        long = "file",
        value_name = "PATH",
        conflicts_with = "message",
        help = "Read the commit message from PATH"
    )]
    message_file: Option<String>,

    #[arg(
        long = "edit",
        short = 'e',
        help = "Open editor for the commit message"
    )]
    edit: bool,
}

#[derive(Debug, Args)]
