use anyhow::Result;
use clap::{Args, Parser, Subcommand, ValueEnum};

use git_subrepo::{
    BranchArgs, CleanArgs, CloneArgs, CommitArgs, ConfigArgs, FetchArgs, InitArgs, JoinMethod,
    PatchesArgs, PatchesStyle, PullArgs, PushArgs, StatusArgs,
};

#[derive(Debug, Parser)]
#[command(name = "git-subrepo")]
#[command(about = "A user-friendly CLI for working with git-subrepo repositories")]
#[command(
    long_about = "A Rust implementation of git-subrepo.\n\nThis CLI focuses on a pleasant interactive experience (help text, clearer errors).\nCore semantics are validated using the upstream compatibility suite via the git-subrepo-compat harness.",
    after_help = "Examples:\n  git subrepo clone https://example.com/repo.git vendor/repo\n  git subrepo pull vendor/repo\n  git subrepo patches vendor/repo\n"
)]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Debug, Subcommand)]
enum Command {
    Clone(CloneCmd),
    Init(InitCmd),
    Fetch(FetchCmd),
    Branch(BranchCmd),
    Pull(PullCmd),
    Push(PushCmd),
    Commit(CommitCmd),
    Status(StatusCmd),
    Clean(CleanCmd),
    Config(ConfigCmd),
    Patches(PatchesCmd),
    Version(VersionCmd),
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, ValueEnum)]
enum JoinMethodValue {
    Merge,
    Rebase,
}

impl From<JoinMethodValue> for JoinMethod {
    fn from(value: JoinMethodValue) -> Self {
        match value {
            JoinMethodValue::Merge => JoinMethod::Merge,
            JoinMethodValue::Rebase => JoinMethod::Rebase,
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, ValueEnum)]
enum PatchesStyleValue {
    Oneline,
    Decorate,
    Stat,
    NameStatus,
}

impl From<PatchesStyleValue> for PatchesStyle {
    fn from(value: PatchesStyleValue) -> Self {
        match value {
            PatchesStyleValue::Oneline => PatchesStyle::Oneline,
            PatchesStyleValue::Decorate => PatchesStyle::Decorate,
            PatchesStyleValue::Stat => PatchesStyle::Stat,
            PatchesStyleValue::NameStatus => PatchesStyle::NameStatus,
        }
    }
}

#[derive(Debug, Args)]
