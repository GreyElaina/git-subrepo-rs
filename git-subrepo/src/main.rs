#[derive(Debug, Args)]
#[command(
    about = "Show local mainline commits affecting a subrepo since the last sync",
    after_help = "Examples:\n  git subrepo patches vendor/repo\n  git subrepo patches vendor/repo --since HEAD~20\n  git subrepo patches vendor/repo --update-ref\n  git subrepo patches --all\n"
)]
struct PatchesCmd {
    #[arg(
        value_name = "SUBDIR",
        conflicts_with_all = ["all", "all_all"],
        help = "Subrepo subdirectory"
    )]
    subdir: Option<String>,

    #[arg(long = "all", help = "List patches for all top-level subrepos")]
    all: bool,

    #[arg(
        long = "ALL",
        help = "List patches for all subrepos, including nested ones"
    )]
    all_all: bool,

    #[arg(
        long = "since",
        value_name = "REV",
        conflicts_with_all = ["from_ref", "since_sync"],
        help = "Start listing patches after REV"
    )]
    since: Option<String>,

    #[arg(
        long = "from-ref",
        value_name = "REF",
        conflicts_with_all = ["since", "since_sync"],
        help = "Start listing patches after REF (any ref name accepted)"
    )]
    from_ref: Option<String>,

    #[arg(
        long = "since-sync",
        conflicts_with_all = ["since", "from_ref"],
        help = "Force commit-message anchor detection (ignore refs/subrepo/<subref>/sync)"
    )]
    since_sync: bool,

    #[arg(
        long = "update-ref",
        help = "Update the baseline ref to the current HEAD"
    )]
    update_ref: bool,

    #[arg(
        long = "ref-name",
        value_name = "REF",
        help = "Ref name to update when using --update-ref (defaults to refs/subrepo/<subref>/baseline)"
    )]
    ref_name: Option<String>,

    #[arg(
        long = "style",
        value_enum,
        default_value = "oneline",
        help = "Output style"
    )]
    style: PatchesStyleValue,

    #[arg(long = "reverse", help = "Reverse output order")]
    reverse: bool,
}

#[derive(Debug, Args)]
#[command(about = "Print version information")]
struct VersionCmd {
    #[arg(short = 'q', long = "quiet", help = "Print only the version number")]
    quiet: bool,
}

fn main() {
    if let Err(err) = try_main() {
        eprintln!("error: {err}");
        std::process::exit(1);
    }
}

fn try_main() -> Result<()> {
    let cli = Cli::parse();

    match cli.command {
        Command::Clone(cmd) => {
            let out = git_subrepo::clone(CloneArgs {
                remote: cmd.remote_url,
                subdir: cmd.subdir,
                branch: cmd.branch,
                force: cmd.force,
                method: cmd.method.into(),
                message: cmd.msg.message,
                message_file: cmd.msg.message_file,
                edit: cmd.msg.edit,
            })?;
            print_out(out);
        }
        Command::Init(cmd) => {
            let out = git_subrepo::init(InitArgs {
                subdir: cmd.subdir,
                remote: cmd.remote,
                branch: cmd.branch,
                method: cmd.method.into(),
            })?;
            print_out(out);
        }
        Command::Fetch(cmd) => {
            let out = git_subrepo::fetch(FetchArgs {
                subdir: cmd.subdir,
                remote: cmd.remote,
                branch: cmd.branch,
                force: cmd.force,
            })?;
            print_out(out);
        }
        Command::Branch(cmd) => {
            let out = git_subrepo::branch(BranchArgs {
                subdir: cmd.subdir,
                force: cmd.force,
                fetch: cmd.fetch,
            })?;
            print_out(out);
        }
        Command::Pull(cmd) => {
            let out = git_subrepo::pull(PullArgs {
                subdir: cmd.subdir,
                force: cmd.force,
                remote: cmd.remote,
                branch: cmd.branch,
                update: cmd.update,
                message: cmd.msg.message,
                message_file: cmd.msg.message_file,
                edit: cmd.msg.edit,
            })?;
            print_out(out);
        }
        Command::Push(cmd) => {
            let out = git_subrepo::push(PushArgs {
                subdir: cmd.subdir,
                force: cmd.force,
                squash: cmd.squash,
                remote: cmd.remote,
                branch: cmd.branch,
                update: cmd.update,
                message: cmd.msg.message,
                message_file: cmd.msg.message_file,
            })?;
            print_out(out);
        }
        Command::Commit(cmd) => {
            let out = git_subrepo::commit(CommitArgs {
                subdir: cmd.subdir,
                commit_ref: cmd.subrepo_ref,
                force: cmd.force,
                fetch: cmd.fetch,
                message: cmd.msg.message,
                message_file: cmd.msg.message_file,
                edit: cmd.msg.edit,
            })?;
            print_out(out);
        }
        Command::Status(cmd) => {
            if cmd.quiet {
                let include_nested = cmd.all_all;
                for s in git_subrepo::subrepos(include_nested)? {
                    println!("{s}");
                }
            } else {
                let out = git_subrepo::status(StatusArgs {
                    subdir: cmd.subdir,
                    all: cmd.all,
                    all_all: cmd.all_all,
                    fetch: cmd.fetch,
                })?;
                print_out(out);
            }
        }
        Command::Clean(cmd) => {
            let removed = git_subrepo::clean(CleanArgs {
                subdir: cmd.subdir,
                force: cmd.force,
            })?;
            if !removed.is_empty() {
                println!("{}", removed.join("\n"));
            }
        }
        Command::Config(cmd) => {
            let out = git_subrepo::config(ConfigArgs {
                subdir: cmd.subdir,
                option: cmd.option,
                value: cmd.value,
                force: cmd.force,
            })?;
            print_out(out);
        }
        Command::Patches(cmd) => {
            let out = git_subrepo::patches(PatchesArgs {
                subdir: cmd.subdir,
                all: cmd.all,
                all_all: cmd.all_all,
                since: cmd.since,
                from_ref: cmd.from_ref,
                since_sync: cmd.since_sync,
                update_ref: cmd.update_ref,
                ref_name: cmd.ref_name,
                style: cmd.style.into(),
                reverse: cmd.reverse,
            })?;
            print_out(out);
        }
        Command::Version(cmd) => {
            if !cmd.quiet {
                println!("{}", git_subrepo::VERSION);
            }
        }
    }

    Ok(())
}

fn print_out(out: String) {
    if !out.is_empty() {
        println!("{out}");
    }
}
